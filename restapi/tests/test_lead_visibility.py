"""
Lead Visibility Tests: Role-based access to leads

Tests the lead visibility scope enforcement for different user roles:
- Users can see only their own created leads or leads generated by them
- Admins can see all leads in their clinic
- Super admins can see all leads in their clinic
"""

from django.test import TestCase
from django.contrib.auth.models import User
from django.db.models import Q

from restapi.models import Clinic, Department, Lead, UserProfile, Role, Employee


class LeadVisibilityTestCase(TestCase):
    """Test role-based lead visibility enforcement at the queryset level"""

    def setUp(self):
        """Set up test fixtures: clinics, roles, users, leads"""
        # ====== CLINICS ======
        self.clinic1 = Clinic.objects.create(name="Clinic Alpha")
        self.clinic2 = Clinic.objects.create(name="Clinic Beta")

